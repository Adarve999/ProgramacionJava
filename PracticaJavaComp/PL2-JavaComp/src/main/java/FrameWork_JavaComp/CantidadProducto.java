    private void ButtonProcederCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonProcederCompraActionPerformed
        // TODO add your handling code here:
        int value = (Integer) jSpinner1.getValue();
        this.setVisible(false);
        try {
            int fila = OpcionesCliente.jTable.getSelectedRow();
            String Titulo = OpcionesCliente.jTable.getValueAt(fila, 0).toString();
            //obtenemos el objeto necesario del producto
            ProductoVenta producto = JavaCompProductos.getProductosTitulo(Titulo);
            
            if (value <= 0) {
                JOptionPane.showMessageDialog(this, "ERROR: ha ocurrido un error Introduzca correctamente la cantidad", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
                this.setVisible(true);
            } else if (value > producto.getStock()) {
                JOptionPane.showMessageDialog(this, "ERROR: la cantidad sobrepasa el stock del producto", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
                this.setVisible(true);
            } else {
                PantallaCompra compra = new PantallaCompra(this);
            }
        } catch (IOException ex) {
            Logger.getLogger(CantidadProducto.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_ButtonProcederCompraActionPerformed
