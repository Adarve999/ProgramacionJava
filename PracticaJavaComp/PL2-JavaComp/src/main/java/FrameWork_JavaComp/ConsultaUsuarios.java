    private void jButtonBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBajaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tablaModel = (DefaultTableModel) ConsultaUsuarios.jTable1.getModel();

        if (ConsultaUsuarios.jTable1.getSelectedRow() != -1) {
            int fila = ConsultaUsuarios.jTable1.getSelectedRow();
            String Titulo = ConsultaUsuarios.jTable1.getValueAt(fila, 1).toString();
            Cliente cliente = JavaCompPersonas.getPersonasTitulo(Titulo);

            if (JavaCompPersonas.bajaPersona(cliente)) {
                JOptionPane.showMessageDialog(this, "Usuario Eliminado Correctamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                tablaModel.removeRow(fila);
            }
        } else {
            JOptionPane.showMessageDialog(this, "ERROR: ha ocurrido un error seleccione el Usuario", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
        }
        ConsultaUsuarios.jTable1.setModel(tablaModel);
        JavaCompPersonas.guardarDatos();

    }//GEN-LAST:event_jButtonBajaActionPerformed

    private void ButtonVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonVisualizarActionPerformed
        // TODO add your handling code here:
        if (ConsultaUsuarios.jTable1.getSelectedRow() != -1) {
            int fila = ConsultaUsuarios.jTable1.getSelectedRow();
            String Titulo = ConsultaUsuarios.jTable1.getValueAt(fila, 1).toString();
            Cliente cliente = JavaCompPersonas.getPersonasTitulo(Titulo);

            if (cliente.getClass().getSimpleName().equals("ClienteParticular")) {
                PantallaRegistroParticular particular = new PantallaRegistroParticular(this);
            } else if (cliente.getClass().getSimpleName().equals("ClienteEmpresas")) {
                PantallaRegistroEmpresas empresa = new PantallaRegistroEmpresas(this);
            }
        } else {
            JOptionPane.showMessageDialog(this, "ERROR: ha ocurrido un error selecione el Usuario", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_ButtonVisualizarActionPerformed

    private void txtFiltroKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyPressed
        // TODO add your handling code here:
        txtFiltro.addKeyListener(new KeyAdapter() {
            public void keyReleased(final KeyEvent e) {
                String cadena = (txtFiltro.getText());
                txtFiltro.setText(cadena);
                FiltroNombre();
            }
        });
        TRSFiltro = new TableRowSorter(ConsultaUsuarios.jTable1.getModel());
        ConsultaUsuarios.jTable1.setRowSorter(TRSFiltro);
    }//GEN-LAST:event_txtFiltroKeyPressed
    private void datosTabla() {
        DefaultTableModel tablaModel = (DefaultTableModel) ConsultaUsuarios.jTable1.getModel();
        JavaCompPersonas.getPersonas().stream().forEach((Cliente cliente) -> {
            try {

                tablaModel.addRow(new Object[]{cliente.getNombre(), cliente.getCorreoElectronico(), cliente.getTelefono(), cliente.getDireccion().getCalle(), cliente.getDireccion().getNumero(), cliente.getDireccion().getCodigoPostal(), cliente.getDireccion().getCiudad()});

//Asociar el modelo de lista al JList
            } catch (Exception e) {
                System.out.println(e.toString());
            }
            ConsultaUsuarios.jTable1.setModel(tablaModel);
        }
        );
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(tablaModel);
        ConsultaUsuarios.jTable1.setRowSorter(sorter);
    }

